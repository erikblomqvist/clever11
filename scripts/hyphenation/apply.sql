	WHERE q.archived_at IS NULL
		AND jsonb_typeof(t.elem) = 'string'
),
matches AS (
	SELECT
		e.question_id,
		e.option_index,
		e.option_text,
		array_agg(w.long_word ORDER BY length(w.long_word) DESC, w.long_word) AS find_words,
		array_agg(w.hyphenated ORDER BY length(w.long_word) DESC, w.long_word) AS replace_words
	FROM expanded e
	JOIN word_map w ON e.option_text LIKE '%' || w.long_word || '%'
	GROUP BY e.question_id, e.option_index, e.option_text
),
applied AS (
	SELECT
		question_id,
		option_index,
		option_text AS current_text,
		find_words,
		replace_words,
		0 AS step
	FROM matches
	UNION ALL
	SELECT
		question_id,
		option_index,
		replace(current_text, find_words[step + 1], replace_words[step + 1]),
		find_words,
		replace_words,
		step + 1
	FROM applied
	WHERE step < array_length(find_words, 1)
),
final AS (
	SELECT DISTINCT ON (question_id, option_index)
		question_id,
		option_index,
		current_text AS new_text
	FROM applied
	ORDER BY question_id, option_index, step DESC
)
-- Rebuild the entire options_json per affected question. This avoids the
-- `UPDATE ãÎ FROM` quirk where multiple source rows joining the same target
-- row collapse to a single update (which would only hyphenate one slot per
-- question, even when several slots needed it).
,per_question AS (
	SELECT
		question_id,
		jsonb_object_agg(option_index::text, to_jsonb(new_text)) AS edits
	FROM final
	GROUP BY question_id
)
UPDATE public.questions q
SET options_json = (
	SELECT jsonb_agg(
		coalesce(p.edits -> ((t.ord - 1)::text), t.elem)
		ORDER BY t.ord
	)
	FROM jsonb_array_elements(q.options_json) WITH ORDINALITY AS t(elem, ord)
)
FROM per_question p
WHERE q.id = p.question_id;

-- Sanity check: number of option-slots that now contain a soft hyphen.
-- Should be substantially higher than 208 (the previous run only hyphenated
-- one slot per question due to the now-fixed UPDATE ãÎ FROM quirk).
SELECT count(*) AS hyphenated_slots
FROM public.questions q
CROSS JOIN LATERAL
	jsonb_array_elements(q.options_json) AS t(elem)
WHERE q.archived_at IS NULL
	AND jsonb_typeof(t.elem) = 'string'
	AND (t.elem #>> '{}') LIKE '%' || chr(173) || '%';
